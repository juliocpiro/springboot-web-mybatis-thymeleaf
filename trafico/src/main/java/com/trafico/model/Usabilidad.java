package com.trafico.model;

public class Usabilidad {
	private String fechaLlamada;
	private String canalVentaAgrup;
	private String app;
	private String web;
	private String otros;
	private String usabilidadApp;
	private String usabilidadWeb;
	private String total;
	public String getFechaLlamada() {
		return fechaLlamada;
	}
	public void setFechaLlamada(String fechaLlamada) {
		this.fechaLlamada = fechaLlamada;
	}
	public String getCanalVentaAgrup() {
		return canalVentaAgrup;
	}
	public void setCanalVentaAgrup(String canalVentaAgrup) {
		this.canalVentaAgrup = canalVentaAgrup;
	}
	public String getApp() {
		return app;
	}
	public void setApp(String app) {
		this.app = app;
	}
	public String getWeb() {
		return web;
	}
	public void setWeb(String web) {
		this.web = web;
	}
	public String getOtros() {
		return otros;
	}
	public void setOtros(String otros) {
		this.otros = otros;
	}
	public String getUsabilidadApp() {
		return usabilidadApp;
	}
	public void setUsabilidadApp(String usabilidadApp) {
		this.usabilidadApp = usabilidadApp;
	}
	public String getUsabilidadWeb() {
		return usabilidadWeb;
	}
	public void setUsabilidadWeb(String usabilidadWeb) {
		this.usabilidadWeb = usabilidadWeb;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
	
}
