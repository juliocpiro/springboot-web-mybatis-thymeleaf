package com.trafico.model;

public class ConvertibilidadRegistro {
	private String ventasApp;
	private String caidasApp;
	private String ventasOtras;
	private String caidasOtras;
	public String getVentasApp() {
		return ventasApp;
	}
	public void setVentasApp(String ventasApp) {
		this.ventasApp = ventasApp;
	}
	public String getCaidasApp() {
		return caidasApp;
	}
	public void setCaidasApp(String caidasApp) {
		this.caidasApp = caidasApp;
	}
	public String getVentasOtras() {
		return ventasOtras;
	}
	public void setVentasOtras(String ventasOtras) {
		this.ventasOtras = ventasOtras;
	}
	public String getCaidasOtras() {
		return caidasOtras;
	}
	public void setCaidasOtras(String caidasOtras) {
		this.caidasOtras = caidasOtras;
	}
	
}
