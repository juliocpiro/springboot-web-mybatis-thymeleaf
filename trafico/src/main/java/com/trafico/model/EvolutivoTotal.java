package com.trafico.model;

public class EvolutivoTotal {
	private String mes;
	private String usabilidad;
	private String despliegue;
	private String ventasApp;
	private String ventasOtras;
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getUsabilidad() {
		return usabilidad;
	}
	public void setUsabilidad(String usabilidad) {
		this.usabilidad = usabilidad;
	}
	public String getDespliegue() {
		return despliegue;
	}
	public void setDespliegue(String despliegue) {
		this.despliegue = despliegue;
	}
	public String getVentasApp() {
		return ventasApp;
	}
	public void setVentasApp(String ventasApp) {
		this.ventasApp = ventasApp;
	}
	public String getVentasOtras() {
		return ventasOtras;
	}
	public void setVentasOtras(String ventasOtras) {
		this.ventasOtras = ventasOtras;
	}
	
}
