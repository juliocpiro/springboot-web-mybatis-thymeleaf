package com.trafico.model;

public class EvolutivoApp {
	private String mes;
	private String app;
	private String otros;
	private String vendApp;
	private String vendOtros;
	private String ventasApp;
	private String ventasOtras;
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getApp() {
		return app;
	}
	public void setApp(String app) {
		this.app = app;
	}
	public String getOtros() {
		return otros;
	}
	public void setOtros(String otros) {
		this.otros = otros;
	}
	public String getVendApp() {
		return vendApp;
	}
	public void setVendApp(String vendApp) {
		this.vendApp = vendApp;
	}
	public String getVendOtros() {
		return vendOtros;
	}
	public void setVendOtros(String vendOtros) {
		this.vendOtros = vendOtros;
	}
	public String getVentasApp() {
		return ventasApp;
	}
	public void setVentasApp(String ventasApp) {
		this.ventasApp = ventasApp;
	}
	public String getVentasOtras() {
		return ventasOtras;
	}
	public void setVentasOtras(String ventasOtras) {
		this.ventasOtras = ventasOtras;
	}
		
}
