package com.trafico.model;

public class EvolutivoWeb {
	private String mes;
	private String web;
	private String otros;
	private String vendWeb;
	private String vendOtros;
	private String ventasWeb;
	private String ventasOtras;
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getWeb() {
		return web;
	}
	public void setWeb(String web) {
		this.web = web;
	}
	public String getOtros() {
		return otros;
	}
	public void setOtros(String otros) {
		this.otros = otros;
	}
	public String getVendWeb() {
		return vendWeb;
	}
	public void setVendWeb(String vendWeb) {
		this.vendWeb = vendWeb;
	}
	public String getVendOtros() {
		return vendOtros;
	}
	public void setVendOtros(String vendOtros) {
		this.vendOtros = vendOtros;
	}
	public String getVentasWeb() {
		return ventasWeb;
	}
	public void setVentasWeb(String ventasWeb) {
		this.ventasWeb = ventasWeb;
	}
	public String getVentasOtras() {
		return ventasOtras;
	}
	public void setVentasOtras(String ventasOtras) {
		this.ventasOtras = ventasOtras;
	}
		
}
