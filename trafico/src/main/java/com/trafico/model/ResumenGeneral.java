package com.trafico.model;

public class ResumenGeneral {
	private String canalVentaAgrup;
	private String vendeApp;
	private String vendeWeb;
	private String total;
	public String getCanalVentaAgrup() {
		return canalVentaAgrup;
	}
	public void setCanalVentaAgrup(String canalVentaAgrup) {
		this.canalVentaAgrup = canalVentaAgrup;
	}
	public String getVendeApp() {
		return vendeApp;
	}
	public void setVendeApp(String vendeApp) {
		this.vendeApp = vendeApp;
	}
	public String getVendeWeb() {
		return vendeWeb;
	}
	public void setVendeWeb(String vendeWeb) {
		this.vendeWeb = vendeWeb;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
}
